package Ex_02_Java_Basics;

public class Lab011_Common_Math {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println(2-2);
        System.out.println("2+2");
        //NOTE: Double quote will treat numbers as String/Character
        //No Mathematical operations will be done
        System.out.println(3/2);
        //NOTE: println will print integer part only not no decimals will be printed
    }
}
